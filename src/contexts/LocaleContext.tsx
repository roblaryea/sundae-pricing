type LocaleContextValue = {
  locale: PricingLocale
  setLocale: (locale: PricingLocale) => void
  dir: 'ltr' | 'rtl'
  messages: (typeof messages)['en']
}

const LocaleContext = createContext<LocaleContextValue | null>(null)

function getInitialLocale(): PricingLocale {
  if (typeof document === 'undefined') return 'en'
  const cookieMatch = document.cookie.match(/(?:^|;\s*)sundae_locale=([^;]+)/)
  if (cookieMatch?.[1]) return normalizeLocale(decodeURIComponent(cookieMatch[1]))
  const storedLocale = window.localStorage.getItem(LOCALE_COOKIE)
  if (storedLocale) return normalizeLocale(storedLocale)
  return normalizeLocale(window.navigator.language)
}

export function LocaleProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<PricingLocale>(() => getInitialLocale())

  useEffect(() => {
    document.documentElement.lang = locale
    document.documentElement.setAttribute('dir', localeDirection[locale])
    document.cookie = `${LOCALE_COOKIE}=${locale}; path=/; max-age=31536000; samesite=lax`
    window.localStorage.setItem(LOCALE_COOKIE, locale)
  }, [locale])

  const value = useMemo<LocaleContextValue>(
    () => ({
      locale,
      setLocale: (nextLocale) => setLocaleState(normalizeLocale(nextLocale)),
      dir: localeDirection[locale],
      messages: messages[locale],
    }),
    [locale]
  )

  return <LocaleContext.Provider value={value}>{children}</LocaleContext.Provider>
}

export function useLocale() {
  const context = useContext(LocaleContext)
  if (!context) {
    throw new Error('useLocale must be used within LocaleProvider')
  }
  return context
}
